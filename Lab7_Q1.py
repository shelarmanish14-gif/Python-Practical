# Program:. Write a Python program to sum all the items in a list. 

# Create a list
list1 = [10, 20, 30, 40, 50]

# Variable to store sum
total = 0

# Add each item
for i in list1:
    total = total + i

# Display result
print("List:", list1)
print("Sum of all items:", total)


#output:-
# List: [10, 20, 30, 40, 50]
# Sum of all items: 150
